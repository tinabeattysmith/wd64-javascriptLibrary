//? Equal
// double equal sign is equivalent

// = = is equal

'3' == 3;
console.log("3" == 3);

/*
    -JavaScript is helpful and does something know as coercion when comparing values
        -coersion is the process of converting a value from one type to another
*/

//? Strict Equal
// triple equal is overrides coersion and evaluates data type

console.log("3" === 3);
console.log(3 === 3);

// === is strictly equal

//? Not Equal
console.log('3' != 3);  // coersion happens


//? Stict Not Equal
console.log('3' !== 3); //overrides coersion and considers data types

//? Greater Than returns a boolean value (true or false)
3 > 2;

//? Less Than returns a boolean value (true or false)
2 < 3

//? Greater Than or Equal To returns a boolean value (true or false)
3 >= 2; // => fat arrow.  This is different then Greater Than/Equal To

//? Less Than or Equal To returns a boolean value (true or false)
2 <= 3; 

//? And value is equal to this AND that
2 && 3

//? Or value is equal to this OR that.  Once returns a true value, it stops.
2 || 3;

