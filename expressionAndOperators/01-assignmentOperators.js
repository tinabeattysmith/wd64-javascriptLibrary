//SUBTRACTION

r -= 2; // r = r - 2
console.log(r)

//MULTIPLICATION
r *= 2;
console.log(r);

//DIVISION
r /= 1;
console.log(r);

//EXPONENTIAL
r **= 2;
console.log(r);

//REMAINDER
r %= 1;
console.log(r)